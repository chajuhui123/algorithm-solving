# Python Numpy에서는 행렬을 어레이(array)로 부른다. 넘파이의 어레이 클래스는 ndarray이다. n-dimensional array란 뜻이다.

# 차원(dimension)은 우리가 일반적으로 얘기하는 행렬의 차원이다.
# 예를 들어서 2차원 어레이는 n행과 m열로 이루어진 n*m행렬이다.
# 3차원 어레이는 행과 열, 그리고 깊이가 있는 n*m*l 행렬을 의미한다. 
# 이런 식으로 하면 4차원 어레이는 행과 열, 깊이, 그리고 페이지로 이루어진 n*m*l*k 행렬이다.

# 보통 행이 1개 또는 열이 1개인 행렬 또는 행렬을 벡터라고 하며 이를 1차원 행렬이라고 하는데, 넘파이 어레이에서는 이들도 모두 2차원 어레이로 분류한다. 
# 1차원 어레이는 행이나 열 표시없이 n개의 성분으로 구성된다.